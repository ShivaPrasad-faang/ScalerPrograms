public class CaseStmt {
    public static void main(String[] args){
        int a=8;
        switch(a){
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("Default Value");
        }
    }
}
